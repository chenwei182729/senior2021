package leetcode01;

public class Code_0050 {
    public double myPow(double x, int n) {
        long N = n;
        if (N < 0) {
            x = 1 / x;
            N = -N;
        }
        return fastPow(x, N);
    }

    private double fastPow(double x, long n) {
        if (n == 0) {
            return 1.0;
        }
        double half = fastPow(x, n >> 1);
        return (n % 2 == 0) ? half * half : half * half * x;
    }
}
